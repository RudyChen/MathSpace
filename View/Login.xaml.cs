    /// <summary>
    /// Login.xaml 的交互逻辑
    /// </summary>
    public partial class Login : Window
    {
        public Login()
        {
            InitializeComponent();
        }

        private void Login_Click(object sender, RoutedEventArgs e)
        {
            var loginVM=this.DataContext as LoginViewModel;
            if (loginVM.IsLoginSucceed)
            {
                this.DialogResult = true;
                this.Close();
            }

        }
    }
